# Estrutura de Decisao if...else

# Quando usar? Quando tiver 2 situacoes

'''
OPERADORES RELACIONAIS

* > Maior
* < Menor
* >= Maior ou igual
* <= Menor ou igual
* == Igualdade
* != Diferença
'''

# Exemplo 1: Verificar e exibir o status do aluno de acor com a media

# media maior ou igual a 7 = APROVADO / media menor do que 7 = REPROVADO
media = float(input("Informe a media do aluno: "))

if (media >= 7):
    print("APROVADO")
else:
    print("REPROVADO")




