# Estrutura de repetição "for"

# quando usar:
    # quando se sabe, exatamente, quantas vezes o bloco de instruções deverá ser utilizado

# exemplo 1: Exibir todos os numeros inteiros de 1 ate 20
for cont in range(1, 21):
    print(cont)

# cont (contador) = variavel 
# in = faz parte do comando for
# range = funão do for - range(inicio, fim)
